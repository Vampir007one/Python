line = input("Введите произвольную строку: ")
print("1.Третий символ:",line[2])
print("2.Предпоследний символ:",line[-2])
print("3.Первые пять символов:",line[:5])
print("4.Вся строка, кроме последних 2-х символов:",line[:-2])
print("5.Все символы с чётными индексами:",line[::2])
print("6.Все символы с нечётными индексами:",line[1::2])
print("7.Все символы в обратном порядке:",line[::-1])
print("8.Все символы через один в обратном порядке:",line[::-2])
print("9.Длина строки: ",len(line))